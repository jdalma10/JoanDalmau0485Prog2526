public class Cosa4 
{
	private int patas;
	private String nombre;

	public Cosa4(String nombre, int patas)
	{
		this.nombre = nombre;
		this.patas = patas;
	}

	public void setPatas(int patas)
	{
		this.patas = patas;
	}
	public int getPatas()
	{
		return patas;
	}
	public void setNombre(String nombre)
		{
			this.nombre = nombre;
		}
	public String getNombre()
	{
		return nombre;
	}
	
	
}

