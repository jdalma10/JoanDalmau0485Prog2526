public class Iteradores
{

  public static void main(String[] args) {
    
    for (int i=0; i<10; i++) {
      System.out.println("Contador 1: " + i);
    }
    
    int cnt = 0;
    while (cnt<10) {
      System.out.println("Contador 2: "+ cnt);
      cnt++;
    }
    
  }
}
